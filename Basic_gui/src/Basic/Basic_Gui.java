    private void writeall() throws IOException {
        BufferedWriter buf = null;
        try {
            buf = new BufferedWriter(new FileWriter(file+getextension(),true));
            buf.write(fn+","+add+","+country+","+gendar+","+dgree);
            buf.newLine();
            JOptionPane.showMessageDialog(rootPane, "Save your all information ");
        } catch (Exception e) {
            e.printStackTrace();
        }
        finally{
            if(buf!=null){
                buf.close();
            }
        }

    }

    private String getextension() {
        String ext = "";
        String extension = fc.getFileFilter().getDescription();
        if (extension.equals("*.txt")) {
            ext = ".txt ";
        }
        return ext;
    }
    private void dispTArea(){
        FileInputStream fobj = null;
        fc = new JFileChooser(dir);
        int option = fc.showOpenDialog(this);
        if(option==JFileChooser.APPROVE_OPTION){
            file=fc.getSelectedFile();
            dir=fc.getCurrentDirectory();
            try {
                String str ="";
                fobj = new FileInputStream(file);
                int len = (int)file.length();
                for(int i=0;i<=len;i++){
                    char str2=0;
                    try {
                        str2=(char)fobj.read();
                        if(str2==','){
                            str2='\t';
                        }
                    } catch (IOException e) {
                        e.printStackTrace();
                    }
                    str=str+str2;
                }
                txt_show.setText(str);
            } catch (Exception e) {
                e.printStackTrace();
            }
            finally{
                try {
                    if(fobj!=null){
                        fobj.close();
                    }
                } catch (Exception e) {
                    e.printStackTrace();
                }
            }
        }
    }
    
