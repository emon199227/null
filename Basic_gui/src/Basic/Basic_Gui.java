    private void btn_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_saveActionPerformed
        // save data :
        getAllRecord();
        if (txt_name.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Please Enter Your Name ");
            txt_name.requestFocus();
        } else {
            if (cmb_country.getSelectedIndex() == 0) {
                JOptionPane.showMessageDialog(rootPane, "Please Select a Country  ");
                cmb_country.requestFocus();
            } else {
                fc = new JFileChooser(dir);
                //add file filter 
                FileNameExtensionFilter filter;
                filter = new FileNameExtensionFilter("*.txt", new String[]{"txt"});
                fc.addChoosableFileFilter(filter);
                int option = fc.showSaveDialog(this);
                if (option == JFileChooser.APPROVE_OPTION) {
                    dir = fc.getCurrentDirectory();
                    //table add row 
                    model.addRow(new Object[]{fn, add, country, gendar, dgree});
                    file = fc.getSelectedFile();
                    try {
                        writeall();
                    } catch (IOException ex) {
                        ex.printStackTrace();
                    }
                    dgree="";
                }
            }
        }
    }//GEN-LAST:event_btn_saveActionPerformed

